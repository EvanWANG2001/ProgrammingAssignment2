## Using these two functions, people can calculate the inverse of a matrix. Also, if the inverse has been calculated, it will return the cached result.

## This function returns a list containing 4 functions that prepare for the inverse calculation and chahe.

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(solve) m <<- solve
        getinverse <- function() m
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
}



## This function calculate the inverse. If the inverse is calculated, it will directly use the previous result.

cacheSolve <- function(x, ...) {
        m <- x$getinverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinverse(m)
        m
}
